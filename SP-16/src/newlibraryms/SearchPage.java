  public void loadDataToTable(){
    
        DefaultTableModel defaultTableModel = new DefaultTableModel(new String[]{"Book ID", "Book Name", "Author Name", "Publisher Name",
             "Stock Number", "Shelf Number", "Column Number", "Row Number","Status"}, 0);
             try {
            
            Object[] tableLines;
            Object[] row = new Object[defaultTableModel.getColumnCount()];
            tableLines =  database.getDataFromFile("BookInfo.txt").toArray();
            for (int i = 0; i < tableLines.length; i++) {

                String lines = tableLines[i].toString();
                String[] splitData = lines.split(",");
                row[0] = splitData[0];
                row[1] = splitData[1];
                row[2] = splitData[2];
                row[3] = splitData[3];
                row[4] = splitData[4];
                row[5] = splitData[5];
                row[6] = splitData[6];
                row[7] = splitData[7];
                if(Integer.parseInt(splitData[4])>0){
                row[8] = "Avilable";
                }else{
                    row[8] = "Not Avilable";
                }
                
                defaultTableModel.addRow(row);
            }
        } catch (IOException ex) {
            Logger.getLogger(Books.class.getName()).log(Level.SEVERE, null, ex);
        }
            
            bookTable.setModel(defaultTableModel);
        }
